#include "libft.h"

int	ft_sqrt(int n)
{
	int	i;

	i = 2;
	if (n < 4)
		return (1);
	while (i <= n / i)
	{
		if (i * i == n)
			return (i);
		i++;
	}
	return (i - 1);
}
